package marsrover.rover;

public class Coordinates {
    private int maxX;
    private int maxY;

    public Coordinates(int maxX, int maxY) {
        this.maxX = maxX;
        this.maxY = maxY;
    }
    public int getMaxX() {
        return maxX;
    }

    public int getMaxY() {
        return maxY;
    }

}
